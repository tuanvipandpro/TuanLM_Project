package tuanlm.car;

import java.io.Serializable;

/**
 *
 * @author Tuan
 */
public class CarDTO implements Serializable{
    private int id;
    private String name;
    private int year, price, quantity;
    private String category, color, date;
    private String from,to;
    private int status;

    public CarDTO() {
    }

    public CarDTO(int id, String name, int year, int price, int quantity, String category, String color, String date, int status) {
        this.id = id;
        this.name = name;
        this.year = year;
        this.price = price;
        this.quantity = quantity;
        this.category = category;
        this.color = color;
        this.date = date;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
